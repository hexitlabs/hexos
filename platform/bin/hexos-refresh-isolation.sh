#!/usr/bin/env bash
set -euo pipefail

# HexOS Phase 1 — Workspace Jail: Cross-Client Isolation Refresh
export PATH="/usr/sbin:/sbin:${PATH}"
# Generates systemd overrides to make each client's dirs inaccessible to other clients
# Run this whenever a client is added or removed.

CLIENT_COUNT=0

for client_dir in /hexos/*/; do
    client=$(basename "$client_dir")
    
    # Skip non-client directories
    [[ "$client" == "platform" || "$client" == "shared" ]] && continue
    
    # Skip if no user exists for this directory
    CLIENT_USER="hexos-${client}"
    if ! id "$CLIENT_USER" &>/dev/null; then
        continue
    fi
    
    CLIENT_COUNT=$((CLIENT_COUNT + 1))
    
    override_dir="/etc/systemd/system/hexos-agent@${client}.service.d"
    mkdir -p "$override_dir"
    
    # Start the override file
    cat > "${override_dir}/isolation.conf" <<EOF
[Service]
# Auto-generated by hexos-refresh-isolation.sh — do not edit manually
# Generated: $(date -Iseconds)
# Blocks this client from accessing other clients' workspaces and platform internals
EOF

    # Add InaccessiblePaths for all OTHER clients
    for other_dir in /hexos/*/; do
        other=$(basename "$other_dir")
        
        # Skip self, platform, and shared
        [[ "$other" == "$client" || "$other" == "platform" || "$other" == "shared" ]] && continue
        
        # Only block dirs that belong to actual clients
        OTHER_USER="hexos-${other}"
        if id "$OTHER_USER" &>/dev/null; then
            echo "InaccessiblePaths=/hexos/${other}" >> "${override_dir}/isolation.conf"
        fi
    done

    # Block platform internals
    echo "InaccessiblePaths=/hexos/platform" >> "${override_dir}/isolation.conf"
done

# Reload systemd to pick up changes
systemctl daemon-reload

if [[ $CLIENT_COUNT -gt 0 ]]; then
    echo "  Isolation refreshed for ${CLIENT_COUNT} client(s)"
else
    echo "  No clients found to refresh"
fi
